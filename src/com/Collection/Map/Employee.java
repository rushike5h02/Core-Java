package com.Collection.Map;

public class Employee {
	
//	Can we take custom  Employee class as key in hashmap	

	
	String id = "10";
	String name = "ram";
	String salary = "5000";

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSalary() {
		return salary;
	}
	
	public void setSalary(String salary) {
		this.salary = salary;
	}

}
