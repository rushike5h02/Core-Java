package com.Polymorphism.MethodOverloading;

public class MethodClass {
	  
	// method Overloading.. has... Same Method Name...diffrent Argument... Same/diff paramerters..
	
	      //         but....Same Class
	
	void m1(int a, int b) {System.out.println("Method m1==="+(a+b));}                // Argument
	
	void m2(double a, double b) {System.out.println("Method m2==="+(a+b));}          // Argument
	
	void m3(int a, int b, int c) {System.out.println("Method m3==="+(a+b+c));}       // Argument
	
	void m4(int a) {System.out.println("Method m4==="+a);}                           // Argument

}
